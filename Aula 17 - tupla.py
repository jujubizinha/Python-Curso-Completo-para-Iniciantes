# Uma tupla em Python é semelhante a uma lista.
# A diferença entre os dois é que não podemos alterar
# os elementos de uma tupla depois de atribuída,
# enquanto podemos alterar os elementos de uma lista.

linguagens = ("Python","Ruby","Javascript","Perl","Haskell")
print(linguagens)

print(linguagens[0:2])
print(linguagens[-1])
print(linguagens[:-2])
print(linguagens[:])
print(len(linguagens))