# Atribuição múltipla é uma operação que atribui o mesmo valor a várias variáveis.
# Em Python, a atribuição múltipla é feita usando o operador de atribuição (=).

#ATRIBUIÇÃO SIMPLES
nome = "Julia"
idade = 19
estudante = True
print(nome)
print(idade)
print(estudante)

#ATRIBUIÇÃO MÚLTIPLA
name, age, student = "Julia", 19, True
print(name)
print(age)
print(student)

